    fn twin(name: &str, class: TwinClass, files: &[&str]) -> ExactTwin {
        use super::super::twins::{TwinClassification, TwinLocation};
        ExactTwin {
            name: name.to_string(),
            locations: files
                .iter()
                .map(|f| TwinLocation {
                    file_path: f.to_string(),
                    line: 1,
                    kind: "function".to_string(),
                    import_count: 0,
                    is_canonical: false,
                    signature_fingerprint: None,
                })
                .collect(),
            signature_similarity: None,
            classification: match class {
                TwinClass::Exact | TwinClass::ShapeSimilar => TwinClassification::Duplicate,
                _ => TwinClassification::Namesake,
            },
            class,
        }
    }

    /// Golden: 30 NAME_COLLISION symbols shared between the same two files
    /// (pack.rs ↔ context/mod.rs smear) collapse to ONE systemic twin-pair
    /// row — not 30 checkboxes — in both the markdown report and the todos.
    #[test]
    fn test_name_collision_pairs_aggregate_per_file_pair() {
        let findings = AuditFindings {
            twins: (0..30)
                .map(|idx| {
                    twin(
                        &format!("shared_symbol_{idx}"),
                        TwinClass::NameCollision,
                        &["src/pack.rs", "src/context/mod.rs"],
                    )
                })
                .collect(),
            ..AuditFindings::default()
        };

        let report = generate_markdown_report(&findings, None);
        let pair_rows = report
            .lines()
            .filter(|l| l.contains("Systemic twin-pair"))
            .count();
        assert_eq!(
            pair_rows, 1,
            "expected ONE aggregated twin-pair row:\n{report}"
        );
        assert!(report.contains("30 shared symbol names"), "{report}");
        assert!(
            report.contains("`src/context/mod.rs` ↔ `src/pack.rs`"),
            "{report}"
        );
        // No consolidation advice for name collisions.
        assert!(!report.contains("Consolidate"), "{report}");

        let todos = generate_todos(&findings, None);
        let todo_rows = todos
            .lines()
            .filter(|l| l.contains("systemic twin-pair"))
            .count();
        assert_eq!(todo_rows, 1, "expected ONE aggregated todo row:\n{todos}");
        assert!(!todos.contains("Consolidate"), "{todos}");
    }

    /// "Consolidate" appears exclusively for class EXACT.
    #[test]
    fn test_consolidate_only_for_exact_class() {
        let findings = AuditFindings {
            twins: vec![
                twin("render_widget", TwinClass::Exact, &["src/a.rs", "src/b.rs"]),
                twin(
                    "register",
                    TwinClass::NameCollision,
                    &["src/c.rs", "src/d.rs"],
                ),
                twin("from_env", TwinClass::Idiom, &["src/e.rs", "src/f.rs"]),
            ],
            ..AuditFindings::default()
        };

        let report = generate_markdown_report(&findings, None);
        let consolidate_lines: Vec<_> = report
            .lines()
            .filter(|l| l.contains("Consolidate"))
            .collect();
        assert!(
            consolidate_lines
                .iter()
                .all(|l| l.contains("render_widget")),
            "Consolidate must only mention the EXACT twin:\n{report}"
        );
        assert!(report.contains("EXACT"), "{report}");
        assert!(report.contains("NAME_COLLISION"), "{report}");
        assert!(report.contains("IDIOM"), "{report}");

        let todos = generate_todos(&findings, None);
        let todo_consolidate: Vec<_> = todos
            .lines()
            .filter(|l| l.contains("Consolidate"))
            .collect();
        assert_eq!(todo_consolidate.len(), 1, "{todos}");
        assert!(todo_consolidate[0].contains("render_widget"), "{todos}");
    }
